#include <stdio.h>
#include <stdlib.h>
#include <windows.h> // 윈도우 콘솔 인코딩 설정을 위해 추가

// 퀴즈 데이터를 담을 구조체 정의
typedef struct {
    char *question;
    char *options[4];
    int answer;
} Question;

// 투자 단계 데이터를 담을 구조체 정의
typedef struct {
    char *name;
    int cost;
} Stage;

int main() {
    // 💡 윈도우 콘솔창의 출력 인코딩을 UTF-8로 변경 (외계어 깨짐 방지)
    SetConsoleOutputCP(65001);

    // 게임 상태 변수 초기화
    int initial_funds = 1000;
    int funds = initial_funds;
    int earned_funds = 0;
    int safety_score = 0;
    int required_safety_score = 30; // 성공을 위한 최소 안전성 점수
    int choice;

    // 생명과학 I 퀴즈 데이터 배열
    Question questions[6] = {
        {"1. [신약 타겟 발굴] 수많은 신약들은 우리 몸의 '물질대사'를 조절해 병을 치료합니다.\n이러한 반응 속도를 조절해주는 단백질인 '생체 촉매'는 무엇일까요?",
         {"1. 호르몬", "2. 효소", "3. 비타민", "4. 무기염류"}, 2},
        {"2. [생명 활동] 세포가 포도당과 같은 영양소를 분해하여\n생명 활동에 필요한 에너지(ATP)를 얻는 과정은?",
         {"1. 광합성", "2. 세포 호흡", "3. 소화 작용", "4. 혈액 순환"}, 2},
        {"3. [항생제 개발] 페니실린 같은 '항생제'를 이용해 치료할 수 있으며,\n결핵이나 식중독을 일으키는 단세포 생물 병원체는 무엇일까요?",
         {"1. 바이러스", "2. 프라이온", "3. 세균(박테리아)", "4. 곰팡이"}, 3},
        {"4. [백신 연구] 질병 예방을 위해 독성을 약화시킨 병원체나 그 일부를\n미리 몸에 주사하여, 인공적으로 면역력을 기억하게 만드는 약품은?",
         {"1. 항생제", "2. 진통제", "3. 백신", "4. 소화제"}, 3},
        {"5. [항원-항체 반응] 우리 몸이 만든 방어 물질인 '항체'가\n침입자인 '항원'과 자물쇠-열쇠처럼 짝이 맞을 때만 결합하는 현상은?",
         {"1. 항원-항체 반응", "2. 알레르기 반응", "3. 혈액 응고 반응", "4. 염증 반응"}, 1},
        {"6. [유전자 분석] 사람의 염색체 46개 중 성별에 관계없이\n공통으로 가지는 44개의 염색체는 무엇일까요?",
         {"1. 상염색체", "2. 성염색체", "3. 상동 염색체", "4. 대립 유전자"}, 1}
    };

    // 신약 개발 투자 단계 배열
    Stage stages[4] = {
        {"기초 연구", 200},
        {"동물 실험", 300},
        {"임상시험", 500},
        {"판매 허가", 400}
    };

    // 시작 화면 출력
    printf("==================================================\n");
    printf("           🧪 신약 개발 퀴즈 타이쿤 🧬           \n");
    printf("==================================================\n");
    printf("생명공학 퀴즈를 풀어 연구비와 안전성 점수를 획득하세요!\n");
    printf("정답을 맞히면 100만 원과 안전성 점수 10점이 지급됩니다.\n\n");
    printf("획득한 자금으로 [기초연구 -> 동물실험 -> 임상시험 -> 판매허가]\n");
    printf("단계를 거쳐 신약 개발을 성공시켜야 합니다.\n\n");
    printf("⚠️ 주의: 최종 판매 허가를 받으려면 안전성 점수가\n최소 %d점 이상이어야 합니다!\n\n", required_safety_score);
    printf("(초기 정부 지원금: 1,000만 원)\n");
    printf("==================================================\n");
    printf("시작하려면 아무 키나 누르고 엔터(Enter)를 치세요...\n");
    getchar(); // 사용자 입력 대기

    // 1단계: 퀴즈 진행
    for (int i = 0; i < 6; i++) {
        printf("\n--------------------------------------------------\n");
        printf("💰 현재 연구비: %d만 원  |  🛡️ 안전성: %d점\n", funds, safety_score);
        printf("--------------------------------------------------\n");
        printf("%s\n", questions[i].question);
        for (int j = 0; j < 4; j++) {
            printf("%s\n", questions[i].options[j]);
        }
        
        printf("\n>> 정답 번호를 입력하세요 (1~4): ");
        scanf("%d", &choice);

        if (choice == questions[i].answer) {
            funds += 100;
            earned_funds += 100;
            safety_score += 10;
            printf("\n✅ 정답입니다! 연구비 100만 원과 안전성 점수 10점을 획득했습니다.\n");
        } else {
            printf("\n❌ 틀렸습니다. 아쉽게도 보상을 획득하지 못했습니다.\n");
        }
    }

    printf("\n==================================================\n");
    printf("모든 퀴즈가 끝났습니다.\n이제 획득한 자금으로 신약 개발 투자를 시작합니다!\n");
    printf("==================================================\n");

    // 2단계: 투자 및 경영 진행
    for (int i = 0; i < 4; i++) {
        printf("\n--------------------------------------------------\n");
        printf("📊 신약 연구 개발 (R&D) - [%s 단계]\n", stages[i].name);
        printf("보유 연구비: %d만 원  |  안전성: %d점\n", funds, safety_score);
        printf("필요 자금: %d만 원\n", stages[i].cost);
        printf("--------------------------------------------------\n");

        printf(">> 투자를 진행하려면 1을 입력하고 엔터를 누르세요: ");
        scanf("%d", &choice);

        // 자금 부족 확인
        if (funds < stages[i].cost) {
            printf("\n💥 [파산] 연구비가 부족합니다!\n");
            printf("필요 자금: %d만 원 / 현재 자금: %d만 원\n", stages[i].cost, funds);
            printf("신약 개발에 실패하여 회사가 파산했습니다.\n");
            return 0; // 게임 오버, 프로그램 종료
        }

        // 마지막 판매 허가 단계에서 안전성 검증
        if (i == 3 && safety_score < required_safety_score) {
            printf("\n🚨 [판매 허가 실패] 안전성 평가에서 탈락했습니다!\n");
            printf("현재 안전성: %d점 / 필요 안전성: %d점\n", safety_score, required_safety_score);
            printf("심각한 부작용 위험으로 인해 신약 출시가 무산되고 회사가 파산했습니다.\n");
            return 0; // 게임 오버, 프로그램 종료
        }

        // 투자 성공
        funds -= stages[i].cost;
        printf("\n🎉 [%s] 단계를 성공적으로 완료했습니다! (차감된 금액: %d만 원)\n", stages[i].name, stages[i].cost);
    }

    // 3단계: 최종 결과 출력
    int company_value = funds * 10 + 5000;
    
    printf("\n==================================================\n");
    printf("               🎉 신약 개발 성공! 🎉               \n");
    printf("==================================================\n");
    printf("엄격한 안전성 평가(%d점)를 통과하고\n", safety_score);
    printf("혁신적인 신약이 시장에 출시되었습니다!\n\n");
    printf("■ 초기 지원금: %d만 원\n", initial_funds);
    printf("■ 퀴즈로 번 연구비: %d만 원\n", earned_funds);
    printf("■ 남은 연구 자금: %d만 원\n\n");
    printf("📈 평가된 제약 회사 가치: %d만 원\n", company_value);
    printf("🌍 사회적 기여도 등급: S등급 (수많은 생명을 구함)\n");
    printf("==================================================\n");

    return 0;
}