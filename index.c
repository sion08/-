#include <stdio.h>
#include <stdlib.h>
#include <windows.h>

// 퀴즈 데이터를 담을 구조체
typedef struct {
    char *question;
    char *options[4];
    int answer;
} Question;

// 투자 단계 데이터를 담을 구조체
typedef struct {
    char *name;
    int cost;
} Stage;

int main() {
    // 윈도우 콘솔창 인코딩 설정 및 한글 깨짐 방지
    system("chcp 65001 > nul"); 
    // 밝은 하늘색 배경(B)에 검은색 글자(0) 적용 (실험실 테마)
    system("color B0"); 

    int initial_funds = 1000;
    int funds = initial_funds;
    int earned_funds = 0;
    int safety_score = 0;
    int required_safety_score = 30;
    int choice;

    Question questions[6] = {
        {"[신약 타겟 발굴]\n수많은 신약들은 우리 몸의 '물질대사'를 조절해 병을 치료합니다.\n이러한 반응 속도를 조절해주는 단백질인 '생체 촉매'는?", {"1. 호르몬", "2. 효소", "3. 비타민", "4. 무기염류"}, 2},
        {"[생명 활동]\n세포가 포도당과 같은 영양소를 분해하여\n생명 활동에 필요한 에너지(ATP)를 얻는 과정은?", {"1. 광합성", "2. 세포 호흡", "3. 소화 작용", "4. 혈액 순환"}, 2},
        {"[항생제 개발]\n페니실린 같은 '항생제'를 이용해 치료할 수 있으며,\n결핵이나 식중독을 일으키는 단세포 생물 병원체는?", {"1. 바이러스", "2. 프라이온", "3. 세균(박테리아)", "4. 곰팡이"}, 3},
        {"[백신 연구]\n질병 예방을 위해 독성을 약화시킨 병원체나 그 일부를\n미리 몸에 주사하여, 면역력을 기억하게 만드는 약품은?", {"1. 항생제", "2. 진통제", "3. 백신", "4. 소화제"}, 3},
        {"[항원-항체 반응]\n우리 몸이 만든 방어 물질인 '항체'가 침입자인 '항원'과\n자물쇠-열쇠처럼 짝이 맞을 때만 결합하는 현상은?", {"1. 항원-항체 반응", "2. 알레르기 반응", "3. 혈액 응고 반응", "4. 염증 반응"}, 1},
        {"[유전자 분석]\n사람의 염색체 46개 중 성별에 관계없이\n공통으로 가지는 44개의 염색체는 무엇일까요?", {"1. 상염색체", "2. 성염색체", "3. 상동 염색체", "4. 대립 유전자"}, 1}
    };

    Stage stages[4] = {
        {"기초 연구", 200},
        {"동물 실험", 300},
        {"임상시험", 500},
        {"판매 허가", 400}
    };

    // ==========================================
    // [화면 1] 시작 화면 
    // ==========================================
    printf("==============================================================\n");
    printf("                  [ 신약 개발 퀴즈 타이쿤 ]                   \n");
    printf("==============================================================\n");
    printf(" [취임 안내] 당신은 제약회사의 신임 CEO로 임명되었습니다!\n");
    printf(" 생명공학 지식을 발휘하여 연구비를 확보하고, 신약을 개발하세요.\n\n");
    printf(" - 진행 방법: 퀴즈 정답 시 연구비 100만원 + 안전성 10점 획득\n");
    printf(" - 개발 과정: [기초연구 -> 동물실험 -> 임상시험 -> 판매허가]\n");
    printf(" - 주의 사항: 판매 허가를 위해 최소 안전성 %d점 이상 필수!\n\n", required_safety_score);
    printf(" (초기 정부 지원금: 1,000만 원)\n");
    printf("==============================================================\n");
    printf(" >> 게임 시작하기 (아무 키나 누르고 Enter): ");
    getchar();

    // ==========================================
    // [화면 2] 퀴즈 화면 
    // ==========================================
    for (int i = 0; i < 6; i++) {
        system("cls"); // 화면 지우기
        
        printf("\n ------------------------------------------------------------\n");
        printf("  [진행도: %d/6]  [연구비: %d만 원]  [안전성: %d점]\n", i+1, funds, safety_score);
        printf(" ------------------------------------------------------------\n\n");

        printf("  +--------------------------------------------------------+\n");
        printf("  | Q. 다음 문제를 읽고 정답을 고르세요.                   |\n");
        printf("  |                                                        |\n");
        printf("    %s\n", questions[i].question);
        printf("  |                                                        |\n");
        printf("  +--------------------------------------------------------+\n\n");

        for (int j = 0; j < 4; j++) {
            printf("    %s\n", questions[i].options[j]);
        }
        
        printf("\n  >> 정답 제출 (1~4 입력): ");
        scanf("%d", &choice);

        printf("\n  ------------------------------------------------------------\n");
        if (choice == questions[i].answer) {
            funds += 100;
            earned_funds += 100;
            safety_score += 10;
            printf("  [알림] 정답입니다! (연구비 +100만 원, 안전성 +10점)\n");
        } else {
            printf("  [알림] 오답입니다. 자금을 획득하지 못했습니다.\n");
        }
        printf("  ------------------------------------------------------------\n");
        printf("  >> 다음으로 넘어가려면 Enter를 누르세요...");
        getchar(); 
        getchar(); 
    }

    // ==========================================
    // [화면 3] 연구개발 투자 화면 
    // ==========================================
    system("cls");
    printf("\n==============================================================\n");
    printf("                 [ 신약 연구 개발 (R&D) 투자 ]                \n");
    printf("==============================================================\n");
    
    for (int i = 0; i < 4; i++) {
        printf("\n  [현재 CEO님의 자금 현황] 연구비 %d만 원 | 안전성 %d점\n", funds, safety_score);
        printf("  ------------------------------------------------------------\n");
        printf("  * 진행 단계 : [%s]\n", stages[i].name);
        printf("  * 필요 자금 : %d만 원\n", stages[i].cost);
        printf("  ------------------------------------------------------------\n");

        printf("  >> 투자를 승인하시겠습니까? (1 입력 시 승인): ");
        scanf("%d", &choice);

        if (funds < stages[i].cost) {
            printf("\n  [시스템] 자금이 부족합니다. (부족분: %d만 원)\n", stages[i].cost - funds);
            printf("  프로젝트가 중단되고 회사가 파산했습니다...\n");
            return 0; 
        }

        if (i == 3 && safety_score < required_safety_score) {
            printf("\n  [시스템] 식약처 판매 허가 반려!\n");
            printf("  이유: 심각한 부작용 우려 (안전성 점수 %d점 미달)\n", required_safety_score);
            printf("  신약 출시가 무산되었습니다...\n");
            return 0; 
        }

        funds -= stages[i].cost;
        printf("\n  [시스템] [%s] 단계 완료! (차감: %d만 원)\n", stages[i].name, stages[i].cost);
        
        if(i < 3) {
            printf("  >> 다음 단계로 넘어가려면 Enter를 누르세요...");
            getchar(); getchar();
            system("cls");
        }
    }

    // ==========================================
    // [화면 4] 최종 결과 화면 
    // ==========================================
    system("cls");
    int company_value = funds * 10 + 5000;
    
    printf("\n==============================================================\n");
    printf("                  [ 신약 개발 최종 성공! ]                    \n");
    printf("==============================================================\n");
    printf(" 위대한 CEO님의 결단과 지식 덕분에 혁신적인 신약이 출시되었습니다!\n\n");
    
    printf("  [경영 성과 요약]\n");
    printf("  - 초기 정부 지원금 : %d만 원\n", initial_funds);
    printf("  - 퀴즈로 번 연구비 : %d만 원\n", earned_funds);
    printf("  - 남은 최종 연구비 : %d만 원\n", funds);
    printf("  - 최종 안전성 점수 : %d점\n\n", safety_score);
    
    printf("  [기업 가치 및 기여도]\n");
    printf("  * 평가된 제약회사 가치 : %d만 원\n", company_value);
    printf("  * 사회적 기여도 평가   : S 등급 (수많은 생명을 구함)\n");
    printf("==============================================================\n");

    return 0;
}